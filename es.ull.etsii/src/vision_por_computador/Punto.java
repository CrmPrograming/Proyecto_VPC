package vision_por_computador;

public class Punto {

  private float a;
  private float b;
  
  public Punto(float x, float y) {
    this.a = x;
    this.b = y;
  }
  
  public float getA() {
    return (a);
  }
  
  public void setA(float a) {
    this.a = a;
  }
  
  public float getB() {
    return (b);
  }
  
  public void setB(float b) {
    this.b = b;
  } 

}
